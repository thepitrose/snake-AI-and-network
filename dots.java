package game;

public class dots {

    private int x;
    private int y;



    public dots(int setx, int sety)
    {
        x=setx;
        y=sety;

    }

    public int getX ()
    {
        return x;
    }

    public int getY ()
    {
        return y;
    }

    public void setX (int xx)
    {
        this.x = xx;
    }

    public void setY (int yy)
    {
        this.y = yy;
    }


}
